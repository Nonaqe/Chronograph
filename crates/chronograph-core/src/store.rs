//! Трейт-граница для слоя хранилища.
//!
//! Реализуется в `chronograph-store` (DuckDB). Ядро пишет сырые данные истории
//! и читает точку инкрементального продолжения только через этот трейт.

use crate::model::{AnalysisMeta, Commit};
use crate::Result;

/// Хранилище сырых данных истории и метаданных прогона.
///
/// Контракт записи **идемпотентен**: повторная запись коммита с тем же SHA не
/// должна дублировать строки (PRIMARY KEY + INSERT OR IGNORE) — это страховка от
/// перезаписи истории при инкрементальном прогоне.
pub trait Store {
    /// `head_sha` последнего успешного прогона, если он есть (из `analysis_meta`).
    ///
    /// Возврат `None` означает «холодный» стор → полный обход истории.
    fn last_head(&self) -> Result<Option<String>>;

    /// Идемпотентно записать коммит и его изменения файлов
    /// (таблицы `commits`, `authors`, `file_changes`).
    ///
    /// `is_mechanical` вычисляется реализацией оркестрации и передаётся флагом,
    /// чтобы стор не знал про пороги.
    fn write_commit(&mut self, commit: &Commit, is_mechanical: bool) -> Result<()>;

    /// Записать метаданные завершённого прогона (таблица `analysis_meta`).
    fn write_meta(&mut self, meta: &AnalysisMeta) -> Result<()>;

    /// Зафиксировать незакоммиченные изменения (commit транзакции / flush).
    fn flush(&mut self) -> Result<()>;
}
